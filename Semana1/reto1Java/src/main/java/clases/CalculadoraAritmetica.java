public class CalculadoraAritmetica {
    
    public int sumar_numeros(int numeroUno, int numeroDos)
    {
        return numeroUno + numeroDos;
    }
    
    public int restar_numeros(int numeroUno, int numeroDos)
    {
        return numeroUno - numeroDos;
    }
    
    public int multiplicar_numeros(int numeroUno, int numeroDos)
    {
        return numeroUno * numeroDos;
    }
    public int dividir_numeros(int numeroUno, int numeroDos)
    {
        return numeroUno / numeroDos;
    }
}
